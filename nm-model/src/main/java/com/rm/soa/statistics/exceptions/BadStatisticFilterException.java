package com.rm.soa.statistics.exceptions;

/**
 * 
 * @author Nabil
 * 
 */
public class BadStatisticFilterException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public BadStatisticFilterException() {
		super();

	}

	public BadStatisticFilterException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

	public BadStatisticFilterException(String message, Throwable cause) {
		super(message, cause);

	}

	public BadStatisticFilterException(String message) {
		super(message);

	}

	public BadStatisticFilterException(Throwable cause) {
		super(cause);

	}

}
