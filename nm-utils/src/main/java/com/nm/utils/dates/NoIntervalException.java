package com.nm.utils.dates;

/**
 * 
 * @author nabilmansouri
 *
 */
public class NoIntervalException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public NoIntervalException() {
		super();
		
	}

	public NoIntervalException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public NoIntervalException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public NoIntervalException(String message) {
		super(message);
		
	}

	public NoIntervalException(Throwable cause) {
		super(cause);
		
	}

}
